	<center><h1>LOJAS AMERICANAS</h1></center>
	<center><i><h2>HISTÓRIA</h2></i></center>
	
	<p>A Lojas Americanas foi fundada em 1929 por John Lee, Glen Matson, James Marshall, Batson Borger (americanos) e Max Landesmann (austríaco) e é uma das principais redes varejistas do Brasil, contando com mais de 1300 estabelecimentos. É controlada por um grupo de 3 pessoas - Jorge Lemann, Marcel Telles e Carlos Sicupira - que também controla a InBev, e atualmente pertence à B2W, grupo que também engloba as empresas Submarino, Shoptime e Soubarato.</p>

	<p>Sua primeira loja foi inaugurada em Niterói, RJ, e tinha o slogan "menos de 2 mil réis", e já contava com 4 lojas no fim do mesmo ano. Em 1994, junto com a Wal Mart Store Inc., formou a joint venture Wal Mart Brasil S/A, que contava com 40% de participação da Lojas Americanas. Três anos depois, o conselho de administração da empresa vendeu esses 40% de participação para focar exclusivamente em suas próprias lojas.</p>

	<p>Essencialmente, a Lojas Americanas trabalha com dois modelos de lojas físicas: o modelo tradicional, com área média de 1500 m² e um catálogo de 60 mil itens, e o Americanas Express, com 400 m² e 15 mil itens. Sua sede continua sendo no Rio de Janeiro e quase 19 mil empregados, a Lojas Americanas alcançou um valor de mercado de R$ 23 bilhões em 2017.</p>
